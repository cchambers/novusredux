-- list of words that only count as bad if they are on their own.
badWordsExact = {
    "hell",
    "spac"
}

function HasBadWords(input)
    local namelower = input:lower()
        
    for i,badWord in pairs(badWords) do
        if(namelower:match(badWord)) then
            return true
        end
    end

    for i,badWord in pairs(badWordsExact) do
        if ( namelower == badWord ) then
            return true
        end
    end

    return false
end

function GetBadWord(input)
    local namelower = input:lower()
        
    for i,badWord in pairs(badWords) do
        if(namelower:match(badWord)) then
            return badWord
        end
    end

    for i,badWord in pairs(badWordsExact) do
        if ( namelower == badWord ) then
            return badWord
        end
    end

    return nil
end